    @Test
    @Transactional
    void createClient() throws Exception {
        int databaseSizeBeforeCreate = clientRepository.findAll().size();
        // Create the Client
        restClientMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(client)))
            .andExpect(status().isCreated());

        // Validate the Client in the database
        List<Client> clientList = clientRepository.findAll();
        assertThat(clientList).hasSize(databaseSizeBeforeCreate + 1);
        Client testClient = clientList.get(clientList.size() - 1);
        assertThat(testClient.getPhoneNumber()).isEqualTo(DEFAULT_PHONE_NUMBER);
        assertThat(testClient.getGender()).isEqualTo(DEFAULT_GENDER);
        assertThat(testClient.getDateOfBirth()).isEqualTo(DEFAULT_DATE_OF_BIRTH);
        assertThat(testClient.getCurrentPlanOffer()).isEqualTo(DEFAULT_CURRENT_PLAN_OFFER);
        assertThat(testClient.getJob()).isEqualTo(DEFAULT_JOB);

        // Validate the id for MapsId, the ids must be same
        assertThat(testClient.getId()).isEqualTo(testClient.getAppUser().getId());
    }

